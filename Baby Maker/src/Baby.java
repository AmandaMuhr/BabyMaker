
public class Baby
	{
		String gender;
		double weight;
		
		public Baby (String g, double w)
		{
			gender = g;
			weight = w;
		}

		public String getGender()
			{
				return gender;
			}

		public void setGender(String gender)
			{
				this.gender = gender;
			}

		public double getWeight()
			{
				return weight;
			}

		public void setWeight(double weight)
			{
				this.weight = weight;
			}
		
	}
